document.getElementById("cards-1").addEventListener("click",function(event) {
    const taskAssigned = document.getElementById("task-assigned").innerText;
    const convertedTaskAssigned = parseFloat(taskAssigned);
    const sub = convertedTaskAssigned - 1 ;
    document.getElementById("task-assigned").innerText=sub;
    const totalAssign = document.getElementById("total-assign").innerText;
    const convertedTotalkAssign = parseFloat(totalAssign);
    const sum = convertedTotalkAssign + 1 ;
    document.getElementById("total-assign").innerText = sum ;

    this.disabled=true;
})
